parse_time_point <- function(x,
                             ...,
                             format,
                             precision,
                             locale,
                             clock) {
  check_dots_empty()

  if (is_null(format)) {
    format <- time_point_precision_format(precision)
  }

  if (!is_clock_locale(locale)) {
    abort("`locale` must be a 'clock_locale' object.")
  }

  labels <- locale$labels
  mark <- locale$decimal_mark

  parse_time_point_cpp(
    x,
    format,
    precision,
    clock,
    labels$month,
    labels$month_abbrev,
    labels$weekday,
    labels$weekday_abbrev,
    labels$am_pm,
    mark
  )
}

